# 加减乘除取余
print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(3 % 4)
print(10 % -4) # 10 = -4 * -4 + -2

# 整除，运算结果只保留整数部分
print(7 // -4)
print(9 // -4)  # 9 = -4 * -3 +-3所以结果为-3

# 幂运算
print(3 ** 4)
print(pow(3 , -2))

